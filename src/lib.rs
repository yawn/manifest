    macro_rules! message_const_format {
        () => {
            "{}_{:05}_{}"
        };
    }

    macro_rules! message_display_format {
        () => {
            "{} ({}_{:05})"
        };
    }

    #[derive(Debug)]
    struct Catalog<T>(IndexMap<u16, T>);

    #[derive(Deserialize)]
    struct Message {
        #[serde(default)]
        comment: Option<String>,
        #[serde(default)]
        deprecated: Option<String>,
        message: String,
        #[serde(default)]
        tags: Option<Vec<String>>,
        #[serde(flatten)]
        extra: IndexMap<String, toml::Value>,
    }

    #[derive(Debug, Default, Deserialize)]
    struct Schema {
        #[serde(default)]
        attributes: IndexMap<String, String>,
        #[serde(default)]
        tags: Option<Vec<String>>,
    }

    impl<T> AsRef<IndexMap<u16, T>> for Catalog<T> {
        fn as_ref(&self) -> &IndexMap<u16, T> {
            &self.0
        }
    }

    impl<'de, T: Deserialize<'de>> Deserialize<'de> for Catalog<T> {
        fn deserialize<D: Deserializer<'de>>(deserializer: D) -> Result<Self, D::Error> {
            struct MapVisitor<T>(PhantomData<T>);

            impl<'de, T: Deserialize<'de>> Visitor<'de> for MapVisitor<T> {
                type Value = Catalog<T>;

                fn expecting(&self, f: &mut fmt::Formatter) -> fmt::Result {
                    f.write_str("a map with unique u16 keys")
                }

                fn visit_map<A: MapAccess<'de>>(self, mut map: A) -> Result<Self::Value, A::Error> {
                    let mut res = IndexMap::new();

                    while let Some(key) = map.next_key::<String>()? {
                        let key: u16 = key.parse().map_err(|_| {
                            serde::de::Error::custom(format!("invalid key: expected u16"))
                        })?;

                        if res.insert(key, map.next_value()?).is_some() {
                            return Err(serde::de::Error::custom(format!(
                                "duplicate key: {}",
                                key
                            )));
                        }
                    }

                    Ok(Catalog(res))
                }
            }

            deserializer.deserialize_map(MapVisitor(PhantomData))
        }
    }

    /// Generate a constant name from a prefix, id, and message
    ///
    /// # Arguments
    /// * `prefix` - The prefix to use (e.g., "MANIFEST")
    /// * `id` - The message ID (u16, 0-65535)
    /// * `message` - The message text
    ///
    /// # Returns
    /// A constant name in the format `{PREFIX}_{ID:05}_{MESSAGE_UPPERCASE_ALPHANUMERIC}`
    ///
    /// # Examples
    /// ```
    /// # use manifest::build::constantize;
    /// assert_eq!(constantize("APP", 1, "user login"), "APP_00001_USER_LOGIN");
    /// assert_eq!(constantize("APP", 42, "login-failed!"), "APP_00042_LOGIN_FAILED_");
    /// ```
    #[cfg_attr(feature = "docs-test", visibility::make(pub))]
    fn constantize(prefix: &str, id: u16, message: &str) -> String {
        let message_part = message
            .to_uppercase()
            .chars()
            .map(|c| if c.is_alphanumeric() { c } else { '_' })
            .collect::<String>();

        format!(message_const_format!(), prefix, id, message_part)
    }

    /// Convert a snake_case or lowercase string to PascalCase for use as an enum variant.
    ///
    /// # Examples
    /// ```
    /// # use manifest::build::pascalize;
    /// assert_eq!(pascalize("security"), "Security");
    /// assert_eq!(pascalize("my_tag"), "MyTag");
    /// ```
    #[cfg_attr(feature = "docs-test", visibility::make(pub))]
    #[cfg_attr(not(feature = "objects"), allow(dead_code))]
    fn pascalize(s: &str) -> String {
        s.split('_')
            .filter(|part| !part.is_empty())
            .map(|part| {
                let mut chars = part.chars();
                match chars.next() {
                    Some(c) => {
                        let mut result = c.to_uppercase().to_string();
                        result.extend(chars);
                        result
                    }
                    None => String::new(),
                }
            })
            .collect()
    }

    /// Generate constants and optionally objects associated with the Manifest.toml file.
    ///
    /// See [include_manifest!] macro for usage details and the generated data structure.
    pub fn generate() {
        println!("cargo::rustc-check-cfg=cfg(no_inline_lookup)");

        let prefix = std::env::var("CARGO_PKG_NAME")
            .expect("CARGO_PKG_NAME not set")
            .to_uppercase();

        let manifest_dir = std::env::var("CARGO_MANIFEST_DIR").expect("CARGO_MANIFEST_DIR not set");
        let input = PathBuf::from(manifest_dir).join("Manifest.toml");

        let out_dir = std::env::var("OUT_DIR").expect("OUT_DIR not set");
        let output = PathBuf::from(out_dir).join("manifest.rs");

        generate_at(&prefix, input, output);
    }

    #[cfg(feature = "objects")]
    fn generate_objects(
        prefix: &str,
        const_map: &IndexMap<String, (&u16, &Message)>,
        schema: &Schema,
    ) -> TokenStream {
        // Generate Tag enum if schema.tags is present
        let tags = if let Some(ref tags) = schema.tags {
            let mut tags = tags.clone();
            tags.sort();

            let variants: Vec<_> = tags
                .iter()
                .map(|v| Ident::new(&pascalize(v), Span::call_site()))
                .collect();

            quote! {
                /// Tag represents a closed set of allowed tags for messages.
                #[derive(Clone, Copy, Debug, Eq, Hash, PartialEq)]
                pub enum Tag {
                    #(#variants),*
                }
            }
        } else {
            quote! {}
        };

        // Sort attribute names for deterministic field order
        let mut attrs: Vec<&String> = schema.attributes.keys().collect();
        attrs.sort();

        let attrs: Vec<_> = attrs
            .iter()
            .map(|name| Ident::new(name, Span::call_site()))
            .collect();

        // Build struct fields and per-message field assignments, all sorted alphabetically
        enum FieldKind {
            Id,
            Message,
            Comment,
            Tags,
            Attribute(Ident),
        }

        let mut fields: Vec<(String, FieldKind)> = vec![
            ("comment".into(), FieldKind::Comment),
            ("id".into(), FieldKind::Id),
            ("message".into(), FieldKind::Message),
            ("tags".into(), FieldKind::Tags),
        ];
        for attr in &attrs {
            fields.push((attr.to_string(), FieldKind::Attribute(attr.clone())));
        }
        fields.sort_by(|(a, _), (b, _)| a.cmp(b));

        let struct_fields: Vec<_> = fields
            .iter()
            .map(|(_, kind)| match kind {
                FieldKind::Id => quote! { pub id: u16 },
                FieldKind::Message => quote! { pub message: &'static str },
                FieldKind::Comment => quote! { pub comment: Option<&'static str> },
                FieldKind::Tags => quote! { pub tags: &'static [Tag] },
                FieldKind::Attribute(attr) => quote! { pub #attr: Option<&'static str> },
            })
            .collect();

        let constants = const_map
            .iter()
            .map(|(constant, (id, message))| {
                let ident = Ident::new(constant, Span::call_site());
                let id = *id;
                let message_text = &message.message;

                let comment = match &message.comment {
                    Some(comment) => quote! { Some(#comment) },
                    None => quote! { None },
                };

                let tags = if let Some(ref tags) = message.tags {
                    let mut tags = tags.clone();
                    tags.sort();
                    let tags: Vec<_> = tags
                        .iter()
                        .map(|t| Ident::new(&pascalize(t), Span::call_site()))
                        .collect();
                    quote! { &[#(Tag::#tags),*] }
                } else {
                    quote! { &[] }
                };

                let field_values: Vec<_> = fields
                    .iter()
                    .map(|(_, kind)| match kind {
                        FieldKind::Id => quote! { id: #id },
                        FieldKind::Message => quote! { message: #message_text },
                        FieldKind::Comment => quote! { comment: #comment },
                        FieldKind::Tags => quote! { tags: #tags },
                        FieldKind::Attribute(attr) => {
                            let value = match message.extra.get(&attr.to_string()) {
                                Some(toml::Value::String(s)) => quote! { Some(#s) },
                                _ => quote! { None },
                            };
                            quote! { #attr: #value }
                        }
                    })
                    .collect();

                quote! {
                    static #ident: Message = Message {
                        #(#field_values,)*
                    };
                }
            })
            .collect::<TokenStream>();

        let lookup_chain = const_map
            .iter()
            .map(|(constant, (id, message))| {
                let ident = Ident::new(constant, Span::call_site());
                let message = &message.message;

                let display = format!(message_display_format!(), message, prefix, id);

                quote! {
                    if equal!(constant, #display) {
                        return &#ident
                    }
                }
            })
            .collect::<TokenStream>();

        let message_display_format = message_display_format!();

        quote! {

            /// Prefix for all messages (which is a constantized version of the crate name).
            const PREFIX: &'static str = #prefix;

            #tags

            /// A Message denotes a message from a message catalogue (defined as Manifest.toml) of immutable messages for audit or business intelligence logging.
            #[derive(Debug, Eq, PartialEq)]
            #[non_exhaustive]
            pub struct Message {
                #(#struct_fields,)*
            }

            impl Message {

                /// Lookup and return a static Message struct reference from its constant equivalent.
                ///
                /// # Panics
                ///
                /// This function will panic if the constant parameter is not defined in the message catalogue.
                #[cfg_attr(not(no_inline_lookup), inline(always))]
                pub(crate) const fn lookup(constant: &'static str) -> &'static Self {

                    use manifest::equal;

                    #constants

                    #lookup_chain

                    panic!("unknown constant");
                }
            }

            impl manifest::Message for &'static Message {
                fn id(&self) -> u16 {
                    self.id
                }

                fn message(&self) -> &'static str {
                    self.message
                }

                fn comment(&self) -> Option<&'static str> {
                    self.comment
                }
            }

            impl std::fmt::Display for Message {
                fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
                    write!(f, #message_display_format, self.message, PREFIX, self.id)
                }
            }

        }
    }

