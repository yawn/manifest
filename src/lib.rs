            assert_eq!(map.get(&1), Some(&"first".to_string()));
            assert_eq!(map.get(&2), Some(&"second".to_string()));
            assert_eq!(map.get(&3), Some(&"third".to_string()));

            let keys: Vec<_> = map.iter().map(|(k, _)| *k).collect();
